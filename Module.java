
/**
 * A class that stores simple information about the module.
 *
 * @author Livanas Dimitris
 * @version 1.0
 */
public class Module
{
    private String name;
    private String code;
    private int credits;
    
    public Module(String name, String code, int credits)
    {
        this.name = name;
        this.code = code;
        this.credits = credits;
    }
}
